    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String NewEmail= newEmail.getText().trim();
        String Mob= mob.getText().trim();
        String Name= name.getText().trim();
        
        if(NewEmail.equals("") || Mob.equals("") || Name.equals("")){
            JOptionPane.showMessageDialog(null, "Fields are required");
        }else{
            if(c.applyJob(email, id, NewEmail, Name, Mob)){
                JOptionPane.showMessageDialog(null, "Applied to job successfully!");
                newEmail.setText("");
                mob.setText("");
                name.setText("");
            }else{
                JOptionPane.showMessageDialog(null, "Error while applying");
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

